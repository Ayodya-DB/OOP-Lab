    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
String name = memberNameTextField.getText();
int months = Integer.parseInt(monthsTextField.getText());

String type;

if (premiumRadioButton.isSelected()) {
    type = "Premium";
} else {
    type = "Regular";
}

boolean newsletter = newsletterCheckBox.isSelected();

Member member = new Member(name, type, newsletter, months);

outputTextArea.setText(
        "Member Name: " + member.getMemberName() + "\n" +
        "Membership Type: " + member.getMembershipType() + "\n" +
        "Months: " + member.getNumberOfMonths() + "\n" +
        "Newsletter: " + member.getNewsletterText() + "\n" +
        "Total Fee: " + member.getMembershipFee()
);        // TODO add your handling code here:
    }//GEN-LAST:event_registerButtonActionPerformed

